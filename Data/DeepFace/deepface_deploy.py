import os
import pandas as pd
from tqdm.auto import tqdm
from deepface import DeepFace

# ===== 1) Paths you need to set =====
IN_CSV = "/content/gdrive/MyDrive/prof_photos/faculty_list.csv"  # <-- CHANGE
PHOTO_DIR = "/content/gdrive/MyDrive/prof_photos/MIT_UCLA_USC"          # <-- CHANGE
OUT_DIR = "/content/gdrive/MyDrive/prof_photos/deepface_outputs"  # <-- CHANGE if needed

os.makedirs(OUT_DIR, exist_ok=True)
OUT_CSV = os.path.join(OUT_DIR, "faculty_photos_with_age_gender.csv")
CACHE_CSV = os.path.join(OUT_DIR, "faculty_photos_with_age_gender_cache.csv")

# ===== 2) Column names in your CSV =====
COL_NAME = "教授名"
COL_SCHOOL = "学校名"
COL_FILE = "照片文件名"

# ===== 3) DeepFace settings =====
DETECTOR_BACKEND = "retinaface"
ENFORCE_DETECTION = True
ACTIONS = ["age", "gender"]
CHECKPOINT_EVERY = 25

# ===== 4) Load data =====
df = pd.read_csv(IN_CSV)
df = df.dropna(subset=[COL_NAME, COL_SCHOOL, COL_FILE]).copy()
print("Rows loaded:", len(df))

# Build full image paths
df["image_path"] = df[COL_FILE].astype(str).apply(lambda x: os.path.join(PHOTO_DIR, x))

# ===== 5) Cache compatibility check =====
if os.path.exists(CACHE_CSV):
    cache_cols = pd.read_csv(CACHE_CSV, nrows=0).columns.tolist()
    if "gender_pred" not in cache_cols:
        print("⚠️ Old cache detected (missing 'gender_pred'). Deleting cache to restart with age+gender analysis...")
        os.remove(CACHE_CSV)
        done_keys = set()
        cache = pd.DataFrame(columns=[
            COL_NAME, COL_SCHOOL, COL_FILE, "image_path",
            "age_pred", "gender_pred", "analyze_status", "error_msg"
        ])
    else:
        cache = pd.read_csv(CACHE_CSV)
        done_keys = set(
            (cache[COL_NAME].astype(str) + "||" + cache[COL_SCHOOL].astype(str) + "||" + cache[COL_FILE].astype(str)).tolist()
        )
        print("Cache found. Already processed:", len(done_keys))
else:
    done_keys = set()
    cache = pd.DataFrame(columns=[
        COL_NAME, COL_SCHOOL, COL_FILE, "image_path",
        "age_pred", "gender_pred", "analyze_status", "error_msg"
    ])

# ===== 6) Analyzer (age + gender) =====
def analyze_age_gender(img_path: str):
    if not os.path.exists(img_path):
        return None, None, "missing_file", "file_not_found"

    try:
        out = DeepFace.analyze(
            img_path=img_path,
            actions=ACTIONS,
            detector_backend=DETECTOR_BACKEND,
            enforce_detection=ENFORCE_DETECTION,
            silent=True
        )

        # Handle return format (list[dict] or dict)
        if isinstance(out, list) and len(out) > 0:
            out = out[0]

        age = out.get("age", None)
        gender = out.get("gender", None)

        # Normalize gender output (handle dict format from some DeepFace versions)
        if isinstance(gender, dict) and gender:
            gender = max(gender, key=gender.get)
        elif isinstance(gender, str):
            # Normalize common variants
            gender = gender.strip().capitalize()
            if gender in ["Male", "Man"]:
                gender = "Man"
            elif gender in ["Female", "Woman"]:
                gender = "Woman"

        return age, gender, "ok", ""
    except Exception as e:
        return None, None, "error", str(e)[:300]

# ===== 7) Main processing loop =====
new_rows = []
for i, row in tqdm(df.iterrows(), total=len(df), desc="DeepFace(age+gender)"):
    name = str(row[COL_NAME]).strip()
    school = str(row[COL_SCHOOL]).strip()
    fname = str(row[COL_FILE]).strip()
    img_path = str(row["image_path"]).strip()

    key = f"{name}||{school}||{fname}"
    if key in done_keys:
        continue

    age, gender, status, err = analyze_age_gender(img_path)

    new_rows.append({
        COL_NAME: name,
        COL_SCHOOL: school,
        COL_FILE: fname,
        "image_path": img_path,
        "age_pred": age,
        "gender_pred": gender,
        "analyze_status": status,
        "error_msg": err
    })

    # Checkpoint saving
    if len(new_rows) >= CHECKPOINT_EVERY:
        cache = pd.concat([cache, pd.DataFrame(new_rows)], ignore_index=True)
        cache.to_csv(CACHE_CSV, index=False, encoding="utf-8-sig")
        done_keys.update([f"{r[COL_NAME]}||{r[COL_SCHOOL]}||{r[COL_FILE]}" for r in new_rows])
        new_rows = []
        tqdm.write(f"✓ Checkpoint saved ({len(cache)} processed)")

# Final flush
if new_rows:
    cache = pd.concat([cache, pd.DataFrame(new_rows)], ignore_index=True)
    cache.to_csv(CACHE_CSV, index=False, encoding="utf-8-sig")
    tqdm.write(f"✓ Final save ({len(cache)} total processed)")

# ===== 8) Merge results back to original dataframe =====
out_df = df.merge(
    cache[[COL_NAME, COL_SCHOOL, COL_FILE, "age_pred", "gender_pred", "analyze_status"]],
    on=[COL_NAME, COL_SCHOOL, COL_FILE],
    how="left"
)
out_df.to_csv(OUT_CSV, index=False, encoding="utf-8-sig")

# ===== 9) Report =====
print("\n" + "="*60)
print("✅ Processing complete!")
print(f"Output saved to: {OUT_CSV}")
print(f"Cache saved to:   {CACHE_CSV}")
print("="*60)
print("\nStatus counts:")
print(out_df["analyze_status"].value_counts(dropna=False))
print("\nGender distribution (successful analyses):")
print(out_df[out_df["analyze_status"] == "ok"]["gender_pred"].value_counts(dropna=False))
print("\nAge statistics (successful analyses):")
print(out_df[out_df["analyze_status"] == "ok"]["age_pred"].describe())
