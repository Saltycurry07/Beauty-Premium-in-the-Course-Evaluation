def choose_folds(n: int, max_folds: int = 5) -> int:
    """
    Keep CV stable: if n is small, reduce folds.
    Needs at least 2 folds.
    """
    if n < 30:
        return 2
    if n < 60:
        return 3
    if n < 120:
        return 4
    return max_folds

def build_preprocess(num_features, cat_features):
    numeric_pipe = Pipeline([
        ("imputer", SimpleImputer(strategy="median")),
        ("scaler", StandardScaler()),
    ])
    categorical_pipe = Pipeline([
        ("imputer", SimpleImputer(strategy="most_frequent")),
        ("onehot", OneHotEncoder(handle_unknown="ignore")),
    ])
    return ColumnTransformer([
        ("num", numeric_pipe, num_features),
        ("cat", categorical_pipe, cat_features),
    ], remainder="drop")

def evaluate_ridge_cv(df_sub: pd.DataFrame, feature_cols_num, feature_cols_cat, seed=0):
    y = df_sub[Y_COL].astype(float)
    X = df_sub[feature_cols_num + feature_cols_cat].copy()

    preprocess = build_preprocess(feature_cols_num, feature_cols_cat)
    model = Ridge(alpha=1.0, random_state=seed)

    pipe = Pipeline([("preprocess", preprocess), ("model", model)])

    k = choose_folds(len(df_sub), MAX_FOLDS)
    cv = KFold(n_splits=k, shuffle=True, random_state=seed)
    scoring = {
        "MAE": "neg_mean_absolute_error",
        "RMSE": "neg_root_mean_squared_error",
        "R2": "r2",
    }

    out = cross_validate(pipe, X, y, cv=cv, scoring=scoring)
    return {
        "folds": k,
        "MAE": float(-out["test_MAE"].mean()),
        "RMSE": float(-out["test_RMSE"].mean()),
        "R2": float(out["test_R2"].mean()),
    }

def plot_scatter_with_fit(df_sub: pd.DataFrame, title: str, out_path: str):
    """
    Scatter: beauty vs avg_rating + red linear fit line.
    Fit is plain least squares on raw x/y (for visualization).
    """
    x = df_sub[BEAUTY_COL].astype(float).values
    y = df_sub[Y_COL].astype(float).values

    plt.figure(figsize=(6, 4.5))
    plt.scatter(x, y, alpha=0.6)

    # Fit line
    if len(df_sub) >= 2 and np.isfinite(x).all() and np.isfinite(y).all():
        b1, b0 = np.polyfit(x, y, 1)  # y = b1*x + b0
        xs = np.linspace(np.min(x), np.max(x), 100)
        ys = b1 * xs + b0
        plt.plot(xs, ys, color="red", linewidth=2)

    plt.title(title)
    plt.xlabel("beauty_1to5")
    plt.ylabel("avg_rating")
    plt.tight_layout()
    plt.savefig(out_path, dpi=200)
    plt.close()

def run_subgroup_analysis(df_all: pd.DataFrame, group_col: str, analysis_name: str):
    """
    For each subgroup value in group_col:
      - run Ridge CV with a consistent feature set (dropping constant col if it's the group var)
      - save scatter + fit plot
      - return metrics table
    """
    results = []

    # Base feature set you used recently (beauty + age + difficulty + gender + school)
    base_num = [BEAUTY_COL, AGE_COL, DIFF_COL]
    base_cat = [SCHOOL_COL, GENDER_COL]

    # If we group by a variable, we should not also include it as a feature (it becomes constant within group).
    num_features = base_num.copy()
    cat_features = base_cat.copy()

    if group_col in cat_features:
        cat_features = [c for c in cat_features if c != group_col]
    if group_col in num_features:
        num_features = [c for c in num_features if c != group_col]

    for gval, df_g in df_all.groupby(group_col):
        df_g = df_g.copy()
        n = len(df_g)
        if n < 20:
            # Too small -> still produce plot, but skip CV (optional)
            plot_path = os.path.join(PLOTS_DIR, f"{analysis_name}_{str(gval).replace('/','-')}_scatter.png")
            plot_scatter_with_fit(
                df_g,
                title=f"{analysis_name}: {gval} (n={n})",
                out_path=plot_path
            )
            results.append({
                "analysis": analysis_name,
                "group": str(gval),
                "n": n,
                "folds": None,
                "MAE": np.nan,
                "RMSE": np.nan,
                "R2": np.nan,
                "plot": plot_path
            })
            continue

        metrics = evaluate_ridge_cv(df_g, num_features, cat_features, seed=RANDOM_SEED)

        plot_path = os.path.join(PLOTS_DIR, f"{analysis_name}_{str(gval).replace('/','-')}_scatter.png")
        plot_scatter_with_fit(
            df_g,
            title=f"{analysis_name}: {gval} (n={n})",
            out_path=plot_path
        )

        results.append({
            "analysis": analysis_name,
            "group": str(gval),
            "n": n,
            "folds": metrics["folds"],
            "MAE": metrics["MAE"],
            "RMSE": metrics["RMSE"],
            "R2": metrics["R2"],
            "plot": plot_path
        })

    return pd.DataFrame(results).sort_values(["analysis", "MAE"], ascending=[True, True])

def plot_mae_bar(df_res: pd.DataFrame, analysis_name: str, out_path: str, top_k: int = 25):
    """
    Bar plot of MAE by subgroup (lower is better). If too many groups, show top_k by MAE.
    """
    sub = df_res[df_res["analysis"] == analysis_name].copy()
    sub = sub.dropna(subset=["MAE"]).sort_values("MAE").head(top_k)

    plt.figure(figsize=(8, max(4, 0.28 * len(sub))))
    plt.barh(sub["group"], sub["MAE"])
    plt.gca().invert_yaxis()
    plt.xlabel("CV MAE (Ridge)")
    plt.title(f"{analysis_name}: Ridge CV MAE by subgroup (top {len(sub)})")
    plt.tight_layout()
    plt.savefig(out_path, dpi=200)
    plt.close()

