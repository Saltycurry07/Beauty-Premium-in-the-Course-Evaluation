import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

from sklearn.model_selection import StratifiedKFold, cross_validate
from sklearn.pipeline import Pipeline
from sklearn.compose import ColumnTransformer
from sklearn.preprocessing import OneHotEncoder, StandardScaler
from sklearn.impute import SimpleImputer

from sklearn.dummy import DummyClassifier
from sklearn.linear_model import LogisticRegression
from sklearn.naive_bayes import GaussianNB

from sklearn.base import BaseEstimator, TransformerMixin

# ---- helper: make sparse -> dense for GaussianNB ----
class ToDense(BaseEstimator, TransformerMixin):
    def fit(self, X, y=None):
        return self
    def transform(self, X):
        return X.toarray() if hasattr(X, "toarray") else X

# =========================
# 0) Load
# =========================
DATA_PATH = "/content/faculty_scored_merged_with_rmp.csv"
df = pd.read_csv(DATA_PATH)
print("Raw rows:", len(df))

# =========================
# 1) Filter analyze_status == ok
# =========================
if "analyze_status" in df.columns:
    mask_ok = df["analyze_status"].astype(str).str.lower().eq("ok")
    df = df.loc[mask_ok].copy()
    print("Dropped analyze_status!=ok:", int((~mask_ok).sum()))
else:
    print("Warning: analyze_status column not found; skip this filter.")

# =========================
# 2) Drop invalid placeholders
# =========================
mask_invalid = (df["avg_rating"] == 0) | (df["avg_difficulty"] == 0)
df = df.loc[~mask_invalid].copy()
print("Dropped invalid (avg_rating==0 or avg_difficulty==0):", int(mask_invalid.sum()))

# =========================
# 3) Drop missing key columns
# =========================
needed_cols = ["avg_rating", "beauty_1to5", "school_name", "age_pred", "gender_pred", "avg_difficulty"]
mask_missing = df[needed_cols].isna().any(axis=1)
df = df.loc[~mask_missing].copy()
print("Dropped missing in needed cols:", int(mask_missing.sum()))
print("Usable rows:", len(df))

# =========================
# 4) Binary label (median split only)
# =========================
y_cont = df["avg_rating"].astype(float)
threshold = float(y_cont.median())
y = (y_cont >= threshold).astype(int)

print("Median threshold:", threshold)
print("Class balance (y=1):", float(y.mean()))

# =========================
# 5) Controlled experiments: feature sets
# =========================
FEATURE_SETS = {
    "F1_beauty_only": {
        "num": ["beauty_1to5"],
        "cat": []
    },
    "F2_add_schoolFE": {
        "num": ["beauty_1to5"],
        "cat": ["school_name"]
    },
    "F3_add_age_gender": {
        "num": ["beauty_1to5", "age_pred"],
        "cat": ["school_name", "gender_pred"]
    },
    "F4_add_difficulty": {
        "num": ["beauty_1to5", "age_pred", "avg_difficulty"],
        "cat": ["school_name", "gender_pred"]
    },
}

# =========================
# 6) Preprocess
#   - Logit: scale numeric
#   - GaussianNB / Dummy: no scaling (optional, but harmless). We'll keep it simple:
#       - Use the same preprocess for all models EXCEPT GaussianNB needs dense.
# =========================
def make_preprocess(num_features, cat_features, scale_numeric=True):
    if scale_numeric:
        numeric_pipe = Pipeline([
            ("imputer", SimpleImputer(strategy="median")),
            ("scaler", StandardScaler()),
        ])
    else:
        numeric_pipe = Pipeline([
            ("imputer", SimpleImputer(strategy="median")),
        ])

    categorical_pipe = Pipeline([
        ("imputer", SimpleImputer(strategy="most_frequent")),
        ("onehot", OneHotEncoder(handle_unknown="ignore")),
    ])

    transformers = []
    if num_features:
        transformers.append(("num", numeric_pipe, num_features))
    if cat_features:
        transformers.append(("cat", categorical_pipe, cat_features))

    return ColumnTransformer(transformers, remainder="drop")

# =========================
# 7) Models
# =========================
models = {
    "DummyMostFrequent": DummyClassifier(strategy="most_frequent", random_state=0),
    "Logit": LogisticRegression(max_iter=3000, solver="lbfgs"),
    "GaussianNB": GaussianNB(),
}

# =========================
# 8) Evaluation (fixed)
# =========================
cv = StratifiedKFold(n_splits=5, shuffle=True, random_state=0)
scoring = {
    "acc": "accuracy",
    "f1": "f1",
    "roc_auc": "roc_auc",
    "precision": "precision",
    "recall": "recall",
}

# =========================
# 9) Run controlled experiments
# =========================
results = []

for exp_name, fs in FEATURE_SETS.items():
    num_features = fs["num"]
    cat_features = fs["cat"]
    X = df[num_features + cat_features].copy()

    for model_name, model in models.items():
        # Use scaling for Logit; no scaling for NB/Dummy (optional but fine)
        preprocess = make_preprocess(
            num_features, cat_features,
            scale_numeric=(model_name == "Logit")
        )

        if model_name == "GaussianNB":
            pipe = Pipeline([
                ("preprocess", preprocess),
                ("dense", ToDense()),
                ("model", model),
            ])
        else:
            pipe = Pipeline([
                ("preprocess", preprocess),
                ("model", model),
            ])

        out = cross_validate(pipe, X, y, cv=cv, scoring=scoring, error_score="raise")

        results.append({
            "experiment": exp_name,
            "model": model_name,
            "n": len(y),

            "acc(mean)": float(out["test_acc"].mean()),
            "acc(std)":  float(out["test_acc"].std()),

            "f1(mean)": float(out["test_f1"].mean()),
            "f1(std)":  float(out["test_f1"].std()),

            "roc_auc(mean)": float(out["test_roc_auc"].mean()),
            "roc_auc(std)":  float(out["test_roc_auc"].std()),

            "precision(mean)": float(out["test_precision"].mean()),
            "precision(std)":  float(out["test_precision"].std()),

            "recall(mean)": float(out["test_recall"].mean()),
            "recall(std)":  float(out["test_recall"].std()),
        })

res = pd.DataFrame(results)

# =========================
# 10) Tables
# =========================
print("\n=== ROC-AUC table (mean) ===")
auc_table = res.pivot(index="model", columns="experiment", values="roc_auc(mean)")
print(auc_table.round(3).to_string())

def fmt(mean, std, nd=3):
    return f"{mean:.{nd}f} ± {std:.{nd}f}"

paper_rows = []
for _, r in res.iterrows():
    paper_rows.append({
        "experiment": r["experiment"],
        "model": r["model"],
        "n": r["n"],
        "acc": fmt(r["acc(mean)"], r["acc(std)"]),
        "f1": fmt(r["f1(mean)"], r["f1(std)"]),
        "roc_auc": fmt(r["roc_auc(mean)"], r["roc_auc(std)"]),
        "precision": fmt(r["precision(mean)"], r["precision(std)"]),
        "recall": fmt(r["recall(mean)"], r["recall(std)"]),
    })

paper_df = pd.DataFrame(paper_rows).sort_values(["experiment", "model"])
print("\n=== Full metrics table (mean ± std) ===")
print(paper_df.to_string(index=False))

# =========================
# 11) Visualization: ROC-AUC grouped bar chart (saved)
# =========================
exp_order = list(FEATURE_SETS.keys())
