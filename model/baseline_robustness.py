plt.figure(figsize=(10.5, 5))
for i, m in enumerate(model_order):
    yvals = plot_df.loc[plot_df["model"] == m, "roc_auc(mean)"].values
    plt.bar(x + offsets[i], yvals, width=bar_width, label=m)

plt.axhline(0.5, linestyle="--")
plt.xticks(x, exp_order)
plt.ylim(0.45, 0.85)
plt.xlabel("Feature set (controlled experiments)")
plt.ylabel("ROC-AUC (5-fold CV mean)")
plt.title("Binary classification: ROC-AUC across feature sets")
